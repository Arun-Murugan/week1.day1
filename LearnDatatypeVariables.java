package week1.day1;

public class LearnDatatypeVariables {
	
	public static void main(String[] args) {
		String mobileName = "Apple";
		char color = 'W';
		long imeiNumber = 56161511516161L;
		boolean isCamera = true;
		boolean isCharger = false;
		float screenSize = 15.5f;
		int price = 300;
		
		System.out.println("Mobile Name: "+mobileName);
		System.out.println("Color: "+color);
		System.out.println("IMEI Number: "+imeiNumber);
		System.out.println("Camera Availability: "+isCamera);
		System.out.println("Include Charger: "+isCharger);
		System.out.println("Screen Size: "+screenSize);
		System.out.println("Prive: $"+price);
	}
}
